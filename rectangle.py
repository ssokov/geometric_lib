def area(a, b):
    '''принимает два значения а и b, возвращает их произведение'''
    '''Пример вызова: area(5, 2)  - вернет 10'''
    return a * b 

def perimeter(a, b): 
    '''принимает два значения а и b, возвращает их сумму умноженную на 2'''
    '''Пример вызова:  perimeter(5, 2)  вернет 14'''
    return 2* (a + b) 