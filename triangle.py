def AreaTriangle(a, h): 
    '''принимает два значения а и h, возвращает их произведение деленное на 2'''
    '''Пример вызова: AreaTriangle(5, 2) - вернет 5'''
    return a * h / 2 

def PerimeterTriangle(a, b, c): 
    '''принимает три значения а, b и с, возвращает их сумму'''
    '''Пример вызова: PerimeterTriangle(5, 2, 3) - вернет 10'''
    return a + b + c 